#!/usr/bin/env bash

# Downloads all proxied artifacts from a GitHub release and generates
# agentbin/checksums.txt with their sha256 hashes.
#
# Usage: generate-embedded-checksums.sh <tag>
# Example: generate-embedded-checksums.sh v0.97.0
#
# The resulting checksums.txt is embedded into the server binary via
# //go:embed and used to verify every artifact proxied from GitHub.

set -euo pipefail

TAG="${1:?Usage: $0 <tag>}"
SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
PROJECT_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
CHECKSUMS_FILE="$PROJECT_ROOT/internal/server/web/api/agentbin/checksums.txt"
WORK_DIR="$(mktemp -d)"

trap 'rm -rf "$WORK_DIR"' EXIT

REPO="${GITHUB_REPOSITORY:-pbs-plus/pbs-plus}"
BASE="https://github.com/${REPO}/releases/download/${TAG}"

# All artifacts the server proxies from GitHub.
# Platform/arch combinations for agent binaries.
PLATFORMS=(
    "windows-amd64"
    "linux-amd64"
    "linux-arm64"
    "freebsd-amd64"
    "freebsd-arm64"
)

ARTIFACTS=()

for P in "${PLATFORMS[@]}"; do
    OS="${P%%-*}"
    ARCH="${P#*-}"

    # Binary filename (with .exe for windows)
    if [ "$OS" = "windows" ]; then
        ARTIFACTS+=("pbs-plus-agent-${TAG}-${P}.exe")
        ARTIFACTS+=("pbs-plus-agent-${TAG}-${P}.exe.sha256")
    else
        ARTIFACTS+=("pbs-plus-agent-${TAG}-${P}")
        ARTIFACTS+=("pbs-plus-agent-${TAG}-${P}.sha256")
    fi

    # Signatures (same name pattern for all OS)
    ARTIFACTS+=("pbs-plus-agent-${TAG}-${P}.sig")
    ARTIFACTS+=("pbs-plus-agent-${TAG}-${P}.ecdsa-sig")
done

# MSI and its checksum
ARTIFACTS+=("pbs-plus-agent-${TAG}-windows-amd64.msi")

echo "Downloading ${#ARTIFACTS[@]} artifacts from release ${TAG}..."

{
    echo "# Auto-generated by generate-embedded-checksums.sh for ${TAG}"
    echo "# Format: filename=sha256"
    echo ""

    for ART in "${ARTIFACTS[@]}"; do
        URL="${BASE}/${ART}"
        DEST="${WORK_DIR}/${ART}"

        echo "  Downloading ${ART}..." >&2
        if curl -sfL -o "$DEST" "$URL"; then
            HASH=$(sha256sum "$DEST" | awk '{print $1}')
            echo "${ART}=${HASH}"
        else
            echo "  WARNING: Failed to download ${ART} (may not exist yet)" >&2
        fi
    done
} > "$CHECKSUMS_FILE"

LINES=$(grep -c '=' "$CHECKSUMS_FILE" || true)
echo "Generated ${CHECKSUMS_FILE} with ${LINES} checksum entries."
